package com.string;

public class IndexMethod {

	public static void main(String[] args) {
		String s="praveen";
		System.out.println(s.indexOf("r"));//	1
		System.out.println(s.lastIndexOf('e'));//5
		System.out.println(s.charAt(4));//r
		System.out.println(s.contains("e"));//true
		System.out.println(s.startsWith("ee"));//false
		System.out.println(s.startsWith("ee"));//false
		System.out.println(s.endsWith("ra"));//false
	
		

	}

}
